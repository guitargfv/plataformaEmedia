package com.grupoemidia.plataformaemedia.model;

public class Dados {
    private int totalDeLeads;
    private int novosLeads;
    private int negociosGanhos;
    private int negociosPerdidos;

    public int getTotalDeLeads() {
        return totalDeLeads;
    }

    public void setTotalDeLeads(int totalDeLeads) {
        this.totalDeLeads = totalDeLeads;
    }

    public int getNovosLeads() {
        return novosLeads;
    }

    public void setNovosLeads(int novosLeads) {
        this.novosLeads = novosLeads;
    }

    public int getNegociosGanhos() {
        return negociosGanhos;
    }

    public void setNegociosGanhos(int negociosGanhos) {
        this.negociosGanhos = negociosGanhos;
    }

    public int getNegociosPerdidos() {
        return negociosPerdidos;
    }

    public void setNegociosPerdidos(int negociosPerdidos) {
        this.negociosPerdidos = negociosPerdidos;
    }
}
